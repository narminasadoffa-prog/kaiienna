async function main() {
  console.log('Начинаем создание demo блог постов...');

  for (const post of demoPosts) {
    try {
      const existingPost = await prisma.blogPost.findUnique({
        where: { slug: post.slug },
      });

      if (existingPost) {
        console.log(`Пост "${post.title}" уже существует, пропускаем...`);
        continue;
      }

      const createdPost = await prisma.blogPost.create({
        data: {
          ...post,
          publishedAt: post.published ? new Date() : null,
        },
      });

      console.log(`✓ Создан пост: ${createdPost.title}`);
    } catch (error) {
      console.error(`Ошибка при создании поста "${post.title}":`, error);
    }
  }

  console.log('Готово! Все demo посты созданы.');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
